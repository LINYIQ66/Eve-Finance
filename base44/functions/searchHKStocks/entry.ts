Deno.serve(async (req) => {
  try {
    const body = await req.json().catch(() => ({}));
    const query = (body.q || '').trim().toLowerCase();

    if (!query || query.length < 1) {
      // Return popular stocks by default
      const popular = Object.entries(POPULAR_NAMES).slice(0, 20).map(([code, name]) => ({
        symbol: code,
        name,
        exchange: "HKEX",
      }));
      return Response.json({ results: popular });
    }

    // Search by code prefix or name match
    const startsWithCode = [];
    const containsCode = [];
    const nameMatch = [];

    for (const code of Object.keys(POPULAR_NAMES)) {
      const name = POPULAR_NAMES[code].toLowerCase();
      if (code.startsWith(query)) {
        startsWithCode.push(code);
      } else if (code.includes(query)) {
        containsCode.push(code);
      } else if (name.includes(query)) {
        nameMatch.push(code);
      }
    }

    const results = [...startsWithCode, ...containsCode, ...nameMatch]
      .slice(0, 20)
      .map(code => ({
        symbol: code,
        name: POPULAR_NAMES[code] || code,
        exchange: "HKEX",
      }));

    return Response.json({ results });
  } catch (error) {
    return Response.json({ error: error.message }, { status: 500 });
  }
});
